n1 = int(
    input("Digite um valor: ")
)  # Solicita ao usuário que digite um valor e converte para inteiro, armazenando na variável 'n1'.
n2 = int(
    input("Digite outro valor: ")
)  # Solicita ao usuário que digite outro valor e converte para inteiro, armazenando na variável 'n2'.
soma = (
    n1 + n2
)  # Realiza a soma dos valores digitados e armazena o resultado na variável 'soma'.
print(
    "A soma entre {} e {} é igual a {}".format(n1, n2, soma)
)  # Imprime a mensagem com os valores digitados e a soma.
